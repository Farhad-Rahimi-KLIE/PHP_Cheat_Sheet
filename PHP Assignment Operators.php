The PHP Assignment operators are used with numeric values to write a value to a variable.

x = y
The left operand gets set to the value of the expression on the right

x = y
x += y
Addition
x = x + y
x -= y
Subtraction
x = x - y
x *= y
Multiplication
x = x * y
x /= y
Division
x = x / y
x %= y
Modulus
x = x % y
